/*=====================================================================================================*
**函數 : Delay_nop
**功能 : 延遲一個while時間
**輸入 : nCnt
**輸出 : None
**使用 : Delay_nop(Times);
**=====================================================================================================*/
/*=====================================================================================================*/
void Delay_nop( vu32 nCnt )
{
  while(nCnt--) {
  }
}
/*=====================================================================================================*/
/*=====================================================================================================*
**函數 : Delay_1us
**功能 : 延遲 1us
**輸入 : nCnt_1us
**輸出 : None
**使用 : Delay_1us(Times);
**=====================================================================================================*/
/*=====================================================================================================*/
void Delay_1us( vu32 nCnt_1us )
{
  u32 nCnt;
  for(; nCnt_1us != 0; nCnt_1us--)
    for(nCnt = 7; nCnt != 0; nCnt--);
}
